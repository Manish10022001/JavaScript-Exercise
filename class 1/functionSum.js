function add(a,b){ // parameters(a,b)
    let c = a+b;    // place where we define the function, that input values are called
    return c;       // parameters. parameters are something that are defined or expected.
}

let x=10;
let y=20;
let result = add(x,y); // arguments(x,y)
console.log(result);    //place where we define the function, that input values are called
                        //arguments. Arguments are something that are defined or expected.

console.log(add(8,3)); //arguments(8,3)